/*------------------------------------------------------------------------------
   グローバル変数の定義
------------------------------------------------------------------------------*/
static int	g_BGMNo = 0;		// BGM識別子

/*------------------------------------------------------------------------------
   初期化関数
------------------------------------------------------------------------------*/
void InitGame(void)
{
	InitPlayer();
	InitEnemy();
	InitBullet();
	InitScore();

	g_BGMNo = LoadSound("data/BGM/battle.wav");
	//Playsound(BGMの番号,0〜254);
	PlaySound(g_BGMNo, 254);
}

/*------------------------------------------------------------------------------
   終了処理をする関数
------------------------------------------------------------------------------*/
void UninitGame()
{
	UninitScore();
	UninitBullet();
	UninitEnemy();
	UninitPlayer();

	StopSoundAll();
}

/*------------------------------------------------------------------------------
   更新処理をする関数
------------------------------------------------------------------------------*/
void UpdateGame(void)
{
	UpdatePlayer();
	UpdateEnemy();
	UpdateBullet();
	UpdateScore();

	UpdateCollision();

	//スペースキーが押されていて、フェード処理中ではないとき
	if (GetKeyboardTrigger(DIK_RETURN) && GetFadeState() == FADE_NONE) {

		//RESULTへ移行する
		SceneTransition(SCENE_RESULT);
	}
}

/*------------------------------------------------------------------------------
   描画処理をする関数
------------------------------------------------------------------------------*/
void DrawGame(void)
{
	DrawBullet();
	DrawEnemy();
	DrawPlayer();
	DrawScore();
}

